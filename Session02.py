# An array can hold many values under a single name
# you can access the values by referring to an index number

# Get the value of the first array item
cars = ["Ford", "Tesla", "BMW"]
x = cars[0]
print(x)

# To modify the value of the first item
cars[0] = "Benz"
print(cars)

# Using a for loop
for x in cars:
    print(x)

# Add an element to the cars array using Append() method
cars.append("Audi")
print(cars)

